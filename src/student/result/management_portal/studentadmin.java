    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here
        String rollno=rollnotf.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver"); //To create an instance to create connections with sql
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","Hardik143"); 
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student inner join result where student.rollNo='"+rollno+"' and result.rollNo='"+rollno+"'");
            if(rs.next()){
                coursenametf.setText(rs.getString(2));
                nametf.setText(rs.getString(4));
                branchnametf.setText(rs.getString(3));
                gendertf.setText(rs.getString(5));
                
                dbmstf.setText(rs.getString(7));
                ostf.setText(rs.getString(8));
                mathstf.setText(rs.getString(9));
                mltf.setText(rs.getString(10));
                pcmitf.setText(rs.getString(11));
                
                int s1=Integer.parseInt(dbmstf.getText());
                int s2=Integer.parseInt(ostf.getText());
                int s3=Integer.parseInt(mathstf.getText());
                int s4=Integer.parseInt(mltf.getText());
                int s5=Integer.parseInt(pcmitf.getText());
                
                int totalmarks=s1+s2+s3+s4+s5;
                totaltf.setText(String.valueOf(totalmarks));
                
                if(s1<30 || s2<30  ||  s3<30 || s4<30  || s5<30){
                    resulttf.setText("FAIL");
                    resulttf.setForeground(Color.red);
                }
                else{
                    resulttf.setText("PASS");
                    resulttf.setForeground(Color.green);
                }
                
            }
        }
        catch(Exception e){
            
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_formComponentShown

