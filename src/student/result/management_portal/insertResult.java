    private void savebtn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebtn2ActionPerformed
        // TODO add your handling code here:
        String Rollno=rollno.getText();
        String Dbms=dbms.getText();
        String Maths=maths.getText();
        String Ml=ml.getText();
        String Os=os.getText();
        String Pcmi=pcmi.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver"); //To create an instance to create connections with sql
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","Hardik143");  // to create a connection with the my database through the given url.
            Statement st=con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollNo='"+Rollno+"'"); //resultset method is a predefined class use to execute sql query. 
            if(rs.next()){
                st.executeUpdate("insert into result values('"+Rollno+"','"+Dbms+"','"+Maths+"','"+Ml+"','"+Os+"','"+Pcmi+"')");  // update query to update the data which is inserted by user.
                JOptionPane.showMessageDialog(null,"Data updated successfully");
                setVisible(false);
                new insertResult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"Roll number does not exist");
            }
            
            
        }
        catch(Exception e){
            
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_savebtn2ActionPerformed

