    private void savebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebtnActionPerformed
        // TODO add your handling code here:
        String course=(String)coursename.getSelectedItem();
        String branch=(String)branchname.getSelectedItem();
        String rollNo=rollno.getText();
        String Name=name.getText();
        String Gender=(String)gender.getSelectedItem();
        
        try{
            
            // to handle exception while connecting db.
            Class.forName("com.mysql.cj.jdbc.Driver"); //To create an instance to create connections with sql
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","Hardik143");  // to create a connection with the my database through the given url.
            Statement st=con.createStatement();  // Creating a a object or st variable.
            st.executeUpdate("insert into student values('"+rollNo+"','"+course+"','"+branch+"','"+Name+"','"+Gender+"')");  //executing update fuction to update the data in the student table using st object.
            JOptionPane.showMessageDialog(null,"Data saved successfully!");  // showing the display message .
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_savebtnActionPerformed

